#include "libft.h"

void	ft_putstab(char **str)
{
	int	i;

	i = 0;
	ft_tablen(str);
	while (i-- > 1)
	{
		ft_putstr((*str));
		ft_putstr(" | ");
		str++;
	}
	ft_putstr((*str));
}
