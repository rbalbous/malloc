#include "libft.h"

int		ft_strnequ(const char *s1, const char *s2, size_t len)
{
	if (!s1 || !s2)
		return (0);
	while ((*s1 != 0 || *s2 != 0) && len-- > 0)
	{
		if (*s2 != *s1)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}
